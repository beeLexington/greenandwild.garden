<section id="data-use">
	<h2>How I use your personal information</h2>

	<p>
		The personal information I collect about you is all necessary for carrying
		out my work as a provider of garden maintenance services. Namely, your
		personal information is used for the following purposes:
	</p>
	<ul>
		<li>
			to safely and accurately fulfil the services you have requested from me
		</li>
		<li>
			to collect and process payment for services I have carried out for you
		</li>
		<li>
			so that I can communicate with you about services you have requested or
			payment for those services
		</li>
		<li>to enable me to research particular plants or problems</li>
	</ul>
	<p>
		With your explicit permission, I may also use some of the information I
		collect in my portfolio and other promotional materials, such as on my
